# backend/engine_adapter.py
"""
Integration seam between the React UI's HTTP contract and the backend team's
audio engine (``cranbrook-audio-project``, aka "hatci-audio-mixer").

The UI posts a rich payload to ``POST /generate`` (see ui/src/payload.js):

    {
      scenario_id, snr_db, speaker_position, seed, car_model,
      driving: 0..3, window: 0..3, venting: 0..3,
      overlays: [{ name, gain?, offset?, location?, velocity? }, ...]
    }

The engine team did NOT build a web service; they built importable DSP:
``mixer.mix_audio(tracks, target_sr)`` mixes N ``(audio, offset_s, gain_db)``
tracks, and ``transform.transform(...)`` does Doppler + binaural HRTF. This
module is the glue: it resolves the payload to real WAV files, builds the
track list, and runs the REAL engine mix. ``backend/app.py`` calls
``generate_from_payload`` and keeps its existing HTTP shape unchanged.

Design choices for this first integrated version:
  * Canonical pipeline rate = 16 kHz mono (matches the existing scenario data
    and metadata; the UI plays whatever WAV it's handed).
  * ``driving`` / ``window`` / ``venting`` actually SELECT samples from raw/
    (the file names encode speed/window/vent), so the UI sliders finally move
    the output. Missing files degrade gracefully (the layer is skipped, never
    a crash).
  * Spatial overlays try the engine's binaural ``transform`` if its native SAF
    library is available; otherwise they fall back to a cheap constant-power
    stereo pan so directional sounds are still lateralized on Windows without
    the CMake/OpenBLAS build.
"""
