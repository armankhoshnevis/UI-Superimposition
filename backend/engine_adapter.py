# ---------------------------------------------------------------------------
# Lazy load of the engine's mixer (importable mix_audio lives in the engine repo)
# ---------------------------------------------------------------------------
_mixer_mod = None


def _load_mixer():
    """Import mixer.mix_audio from the engine repo by file path.

    mixer.py does ``import librosa`` at module top but only uses it in its CLI
    main(); the importable ``mix_audio`` does not. librosa has no wheels on this
    env's Python yet, so we install an empty stub module ONLY when it's missing,
    letting the genuine engine code import and run for the core mix path.
    """
    global _mixer_mod
    if _mixer_mod is not None:
        return _mixer_mod

    if importlib.util.find_spec("librosa") is None and "librosa" not in sys.modules:
        sys.modules["librosa"] = types.ModuleType("librosa")

    path = os.path.join(_ENGINE_ROOT, "mixer", "mixer.py")
    if not os.path.exists(path):
        raise FileNotFoundError(f"engine mixer not found at {path}")
    spec = importlib.util.spec_from_file_location("cranbrook_mixer", path)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    _mixer_mod = module
    return module


# ---------------------------------------------------------------------------
# File-resolution helpers (all degrade gracefully to None)
# ---------------------------------------------------------------------------
def _raw(*parts):
    return os.path.join(_REPO_ROOT, "raw", *parts)


def _first_existing(*candidates):
    """Return the first candidate path that exists, else None."""
    for c in candidates:
        if c and os.path.exists(c):
            return c
    return None


def _swap_window_suffix(filename, window):
    """Rewrite a ``..._w{N}.wav`` file name to the requested window state."""
    return re.sub(r"_w\d(?=\.wav$)", f"_w{window}", filename)


def _select_road_noise(driving, window):
    """Pick a road-noise sample for the given driving/window, or None."""
    speed = _DRIVING_TO_SPEED.get(int(driving), "s50")
    if speed == "s0" and int(driving) == 0:
        # At rest: no road noise layer.
        return None
    base = _raw("distributed", "noise")
    # Try exact, then any window for that speed, then any ver-suffixed variant.
    exact = os.path.join(base, f"{speed}_w{window}.wav")
    by_speed = sorted(glob.glob(os.path.join(base, f"{speed}_w*.wav")))
    return _first_existing(exact, *(by_speed or []))


def _select_ventilation(venting, window):
    """Pick a ventilation sample for the given vent level, or None."""
    if not venting:
        return None
    base = _raw("distributed", "ventilation")
    # Vent files only exist for w0 / w2; snap the window to the nearest.
    snapped = 0 if int(window) < 2 else 2
    cand = os.path.join(base, f"v{int(venting)}_w{snapped}.wav")
    any_v = sorted(glob.glob(os.path.join(base, f"v{int(venting)}_w*.wav")))
    return _first_existing(cand, *(any_v or []))


def _resolve_cabin_ir(scenario, window):
    """Resolve the cabin IR path, swapping its window suffix to match the UI."""
    ingredients = scenario.get("audio_ingredients", {})
    ir = ingredients.get("cabin_ir", {}).get("file")
    if not ir:
        return None
    requested = _swap_window_suffix(ir, window)
    return _first_existing(_raw(requested), _raw(ir))


# ---------------------------------------------------------------------------
# Cheap stereo pan fallback for spatial overlays (used when the native SAF
# binaural lib isn't built). location.y is +right per ui/src/payload.js.
# ---------------------------------------------------------------------------
_PAN_FULL_SCALE_M = 2.4  # EXT_SOURCE_OFFSET_M in payload.js — maps to hard L/R


def _pan_to_stereo(mono, pan):
    """Constant-power pan a mono signal. pan in [-1, 1] (-1 left, +1 right)."""
    pan = float(np.clip(pan, -1.0, 1.0))
    angle = (pan + 1.0) * (np.pi / 4.0)  # 0..pi/2
    left = mono * np.cos(angle)
    right = mono * np.sin(angle)
    return np.stack([left, right], axis=1).astype(np.float32)


def _try_engine_transform(file_path, location, velocity):
    """Attempt the engine's Doppler+binaural transform; return stereo@TARGET_SR.

    Returns None on ANY failure (missing native SAF lib, no ffmpeg, etc.) so the
    caller can fall back. Enabling real binaural later is just building the lib;
    no code change here.
    """
    try:
        tdir = os.path.join(_ENGINE_ROOT, "transform")
        if tdir not in sys.path:
            sys.path.insert(0, tdir)
        spec = importlib.util.spec_from_file_location(
            "cranbrook_transform", os.path.join(tdir, "transform.py")
        )
        tmod = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(tmod)

        speed = float(np.linalg.norm([velocity.get("x", 0), velocity.get("y", 0), velocity.get("z", 0)]))
        direction = (velocity.get("x", 1.0) or 1.0, velocity.get("y", 0.0), velocity.get("z", 0.0))
        initial = (location.get("x", 0.0), location.get("y", 0.0), location.get("z", 0.0))
        stereo, sr = tmod.transform(file_path, initial_pos=initial, direction=direction, speed=speed or 1.0)

        if sr != TARGET_SR:  # transform outputs 48 kHz; bring it back to canonical
            from scipy.signal import resample
            n = int(stereo.shape[0] * TARGET_SR / sr)
            stereo = np.stack([resample(stereo[:, 0], n), resample(stereo[:, 1], n)], axis=1)
        return stereo.astype(np.float32)
    except Exception as e:  # noqa: BLE001 — intentional broad guard; spatial is best-effort
        print(f"[engine_adapter] spatial transform unavailable, using pan fallback: {e}")
        return None


# ---------------------------------------------------------------------------
# Track builders
# ---------------------------------------------------------------------------
def _load_norm(path):
    """Load a file as mono @TARGET_SR, normalized to the reference RMS."""
    audio, _ = load_wav(path)
    return normalize_rms(audio, SPEECH_RMS_DB)


def _overlay_track(overlay):
    """Build one (audio, offset_s, gain_db) tuple from a UI overlay, or None."""
    name = overlay.get("name")
    rel = SOUND_FILE_MAP.get(name)
    if not rel:
        print(f"[engine_adapter] overlay '{name}' has no mapped sample, skipping")
        return None
    path = _raw(rel)
    if not os.path.exists(path):
        print(f"[engine_adapter] mapped file missing for '{name}': {path}, skipping")
        return None

    gain_db = float(overlay.get("gain", 0.0))
    offset = float(overlay.get("offset", 0.0))
    location = overlay.get("location")
    velocity = overlay.get("velocity")

    if location:
        stereo = None
        if velocity:  # moving source: try real Doppler + HRTF
            stereo = _try_engine_transform(path, location, velocity)
        if stereo is None:  # fallback: constant-power pan from lateral position
            mono = _load_norm(path)
            pan = float(location.get("y", 0.0)) / _PAN_FULL_SCALE_M
            stereo = _pan_to_stereo(mono, pan)
        return (stereo, offset, gain_db)

    # Non-spatial (global ambient) overlay: plain mono track.
    return (_load_norm(path), offset, gain_db)


